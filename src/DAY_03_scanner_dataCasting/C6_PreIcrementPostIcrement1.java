package DAY_03_scanner_dataCasting;

public class C6_PreIcrementPostIcrement1 {

	public static void main(String[] args) {
		
		
		int a =13;
		
		a++;
		System.out.println(a);
//		14

		
		a--;
		System.out.println(a);
		//13
		
		a*=3;
		System.out.println(a);
		//39
		
		
		a/=3;
		System.out.println(a);
		//13

		++a;
		--a;
		System.out.println(a++);
		System.out.println(a);
		System.out.println(++a);
//		13
//		14
//		15
	}

}
